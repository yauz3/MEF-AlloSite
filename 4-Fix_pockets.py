def fix_pocket(protein_list,output_name):
    """
    The code will take residues info from Fpokcet output and check whether incomplete residue exist on pocket or not. if
    it finds incomplete resiude, it will complete that residue.
    :param protein_list:
    :param output_name:
    :return:
    """

    for protein in protein_list:
        # check whether PROTEIN_NAME_fixed directory exist or not
        fix_protein_path = f"{current_dir}/data/fix_pockets/{output_name}/{protein}_fixed"
        # if not, create one to score fixed pockets
        if not os.path.exists(fix_protein_path):
            os.makedirs(fix_protein_path)
        # find the pockets found by Fpokcet
        os.chdir(f"{current_dir}/data/{output_name}/{protein}_cleaned_out/pockets")
        # take all pocket for each protein input
        pocket_list=glob.glob("*.pdb")
        # extracts the base name (without extension) from each file name in pocket_list and stores them in a new list called pocket_list
        pocket_list = [os.path.splitext(file)[0] for file in pocket_list]
        # take the residues for pockets, then check whether residues having missing parts or not. If there is a missing parts, take them from proteins and fix pockets
        for pocket in pocket_list:
            fix_fpocket_pockets_working.neigh_residue(pocket_name=pocket,
                                                      protein_input=protein,
                                                      input_path_pocket=f"{current_dir}/data/{output_name}/{protein}_cleaned_out/pockets",
                                                      input_path_protein=f"{current_dir}/data/pdbs",
                                                      output_path=fix_protein_path)

